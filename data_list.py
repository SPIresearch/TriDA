class ImageList_neighbor(Dataset):
    def __init__(self, image_list, indices, labels=None, transform=None, target_transform=None, mode='RGB'):
        imgs = make_dataset(image_list, labels)
        if len(imgs) == 0:
            raise(RuntimeError("Found 0 images in subfolders of: " + root + "\n"
                               "Supported image extensions are: " + ",".join(IMG_EXTENSIONS)))

        self.imgs = imgs
        self.transform = transform
        self.target_transform = target_transform
        self.indices = indices
        if mode == 'RGB':
            self.loader = rgb_loader
        elif mode == 'L':
            self.loader = l_loader

    def __getitem__(self, index):
        path, target = self.imgs[index]
        img = self.loader(path)
        neighbor_index = np.random.choice(self.indices[index], 1)[0]

        neighbor_path, neighbor_target = self.imgs[neighbor_index]
        neighbor = self.loader(neighbor_path)

        if self.transform is not None:
            img = self.transform(img)
            neighbor = self.transform(neighbor)

        if self.target_transform is not None:
            target = self.target_transform(target)

        return img, neighbor, target, torch.from_numpy(self.indices[index])

    def __len__(self):
        return len(self.imgs)